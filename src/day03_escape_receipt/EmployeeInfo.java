package day03_escape_receipt;

public class EmployeeInfo {
    public static void main (String [] args){
        System.out.println("Company name: \tGoogle");
        System.out.println("Employee :    \tKhalid");
        System.out.println("ID:            \t12345");
        System.out.println("Job Title:    \tSDET");
        System.out.println();
        System.out.println("----------------------------------");

        System.out.println("Company name:\tGoogle\nEmployee:\t\tKhalid\nID:\t\t\t\t12345\nJob Title: \t\t SDET");
    }
}
