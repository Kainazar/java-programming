package allDaysJava.day09_scanner_practice;

public class RemainderOperator {
    public static void main(String[] args) {
        System.out.println( 10 / 3); // 3
        System.out.println( 10 % 3); // 1  //3 + 3 + 3 + 1
        System.out.println( 10 / 5); // 2
        System.out.println( 10 % 5); // 0
        System.out.println( 20 / 7); // when you are divide whole number
        System.out.println( 20 % 7); // always will show you remainder answer is 6.


    }
}
