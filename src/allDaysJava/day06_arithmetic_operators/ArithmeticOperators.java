package allDaysJava.day06_arithmetic_operators;

public class ArithmeticOperators {
    public static void main(String[] args){
        System.out.println(5 + 10); //15
        System.out.println(10 - 3); //7
        System.out.println(10 * 3); //30
        System.out.println(21 / 7); //3
        System.out.println(15 % 3); //5
        System.out.println(12 % 5);
        // 5+5+2 is remainder
        System.out.println(-2 *  2);//-4
        int chairs = 44444 + 222222 ;
        System.out.println(chairs);
        int plates = 10 + 20 +12;
        System.out.println(plates);
        int cars = 12 + 5 - 2 * 2;
        System.out.println(cars);
        int oranges = 5 + 5 - 2 * 2;
        System.out.println(oranges);
        int cakes = 15 / 5 + 3 % 2;
        System.out.println(cakes);
        // There are 42 spoons in  kitchen
        System.out.println("There are " + "spoons" + "spoons in kitchen");





    }
}
