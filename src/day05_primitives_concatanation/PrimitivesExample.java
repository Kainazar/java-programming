package day05_primitives_concatanation;

public class PrimitivesExample {
    public static void main(String[] args) {
        // ****** WHOLE NUMBERS / INTEGER TYPES******
        byte byteValue = 125;
        short shortValue = 32000;
        int intValue = 2131231231;
        long longValues = 575725252557575L;
        // DECIMAL NUMBERS / FLOAT NUMBERS POINT TYPES *****
        float floatValue = 1234.56F;
        double doubleValue = 3563.3642543; // UP TO ME D/d
        char charValue = 'B';
        // ****** Boolean ****** true or false *******
        boolean booleanValue = true;
        System.out.println(byteValue);
        System.out.println(shortValue);
        System.out.println(intValue);
        System.out.println(longValues);
        System.out.print(floatValue);
        System.out.println(doubleValue);
        System.out.println(charValue);
        System.out.println(booleanValue);
       // char charValue = 66,97,107,121.116






    }
}
